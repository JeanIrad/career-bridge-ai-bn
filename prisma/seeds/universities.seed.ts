export async function seedUniversities() {
  console.log('üè´ Seeding universities...');

  try {
    // Clear existing universities
    // await prisma.university.deleteMany({});

    // Create universities
    for (const university of universities) {
      // await prisma.university.create({
      //   data: university,
      // });
      await prisma.university.upsert({
        where: {
          name: university.name,
        },
        update: {
          ...university,
          isActive: true,
          isVerified: true,
        },
        create: university,
      });
      console.log(`‚úÖ Created university: ${university.name}`);
    }

    console.log(`üéâ Successfully seeded ${universities.length} universities`);
  } catch (error) {
    console.error('‚ùå Error seeding universities:', error);
    throw error;
  }
}
